# Object Oriented Programming in Python

# Class: A blueprint for creating objects. Classes can contain methods (functions) and attributes (similar to keys in a dictionary).

# Instance: Objects that are constructed from a class blueprint that contain their class's methods and properties.

# Encapsulation: One of the fundamental concepts in OOP, encapsulation is the bundling of data with the methods that operate on that data. Encapsulation is used to restrict access to some of the object's components.

# Inheritance: A way to form new classes using classes that have already been defined. The newly formed classes are called derived classes, the classes that we derive from are called base classes.

# Polymorphism: A way to define methods in a child class that have the same name as the methods in the parent class. In inheritance, polymorphism allows us to define methods in the child class with the same name as defined in their parent class.

